# step argument mtlb ek number se dusre number tk jane me kitan step le rha hain 
# 1 to 10 ----1,2,3, 4, step 1
# 1 to 10------1, 3, 5, step 2

for i in range(1,11,1): #  1 step
    print(i)

for i in range(1,11,2): #even number 2 step lekr kr skte hain 
    print(i)

for i in range(0,11,2): # odd number print krane keliye 0 se start or 2 step lekr
    print(i)


